# ── RTL rendering helpers ──────────────────────────────────────────────────────

_TH = (
    "padding:8px 14px;text-align:right;border-bottom:2px solid #e2e8f0;"
    "background:#f8fafc;font-weight:600;"
)
_TD = "padding:8px 14px;text-align:right;border-bottom:1px solid #f1f5f9;"
_TABLE_STYLE = "width:100%;border-collapse:collapse;margin:0.6rem 0;font-size:0.95rem;"
_DIV_STYLE = (
    "direction:rtl;text-align:right;"
    "font-family:'Segoe UI',Tahoma,Arial,sans-serif;"
    "font-size:1rem;line-height:1.95;padding:0.2rem 0;"
)


def _r(html: str) -> None:
    """Render an HTML block with RTL direction."""
    st.markdown(
        f'<div style="{_DIV_STYLE}">{html}</div>',
        unsafe_allow_html=True,
    )


def _tbl(headers: list[str], rows: list[list[str]]) -> str:
    """Build a styled RTL HTML table string."""
    ths = "".join(f"<th style='{_TH}'>{h}</th>" for h in headers)
    trs = "".join(
        "<tr>" + "".join(f"<td style='{_TD}'>{c}</td>" for c in row) + "</tr>"
        for row in rows
    )
    return f"<table style='{_TABLE_STYLE}'><thead><tr>{ths}</tr></thead><tbody>{trs}</tbody></table>"


def _section(icon: str, title_ar: str, content_fn, expanded: bool = False) -> None:
    with st.expander(f"{icon}  {title_ar}", expanded=expanded):
        content_fn()


def _tip(text: str) -> None:
    _r(f"<div style='background:#eff6ff;border-right:4px solid #3b82f6;"
       f"padding:10px 14px;border-radius:4px;margin:8px 0;'>"
       f"💡 {text}</div>")


def _warn(text: str) -> None:
    _r(f"<div style='background:#fffbeb;border-right:4px solid #f59e0b;"
       f"padding:10px 14px;border-radius:4px;margin:8px 0;'>"
       f"⚠️ {text}</div>")


def _note(text: str) -> None:
    _r(f"<div style='background:#f0fdf4;border-right:4px solid #22c55e;"
       f"padding:10px 14px;border-radius:4px;margin:8px 0;'>"
       f"ℹ️ {text}</div>")


